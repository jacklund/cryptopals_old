    // Nineteenth cryptopals challenge - https://cryptopals.com/sets/3/challenges/19
    // Note: I didn't exactly follow the directions - I came to the conclusion that the
    // solution was similar to the repeating xor on my own, so this is much closer to the 20th
    // one (although my solution is better, IMHO, because I guess the entire keystream, not just
    // the shortest part).
    // Interestingly, the first letter is wrong in both, and (not surprisingly), there are more errors
    // towards the end, where the number of samples is smaller.
    #[test]
    fn challenge19() {
        let key = "YELLOW SUBMARINE".as_bytes();
        let plaintexts: Vec<Vec<u8>> = CTR_BREAKING_PLAINTEXTS
            .iter()
            .map(|p| base64::decode(p).unwrap())
            .collect();

        let ciphertexts: Vec<Vec<u8>> = plaintexts
            .iter()
            .map(|p| ctr(key, &iter::repeat(0u8).take(8).collect::<Vec<u8>>(), &p).unwrap())
            .collect();

        // Interestingly, the plaintexts aren't exactly the same, but are similar enough
        // to be able to read. Mostly, (very interestingly), there's just a lower-case letter
        // where there should be the corresponding upper-case one, or a minor error which is
        // still readable
        for (index, decrypted) in break_ctr(&ciphertexts).iter().enumerate() {
            println!("{}", str::from_utf8(&decrypted).unwrap());
            println!("{}", str::from_utf8(&plaintexts[index]).unwrap());
        }
    }
